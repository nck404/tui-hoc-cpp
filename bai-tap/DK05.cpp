#include <stdio.h>
#include <math.h>
int main()
{
    long long n;
    bool k;
    scanf("&d", n);

    for (size_t i = 0; i <= n+1; i++)
    {
        if (n = i*i)
        {
            k = true;
        } else k = false;
    }
    if (k = true)
    {
        printf("TRUE");
    } else printf("NO");
    
    
}